public class T4DoWhile {
    public static void main(String[] args) {
        // Print even numbers from 20 to 100 using "do" "while"
        // Expected output 20 22 24 26...100

        int i=20;

        do {
            System.out.println(i + " ");
            i=i+2;
        }while (i<=100);

        }
        }

