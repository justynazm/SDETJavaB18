package review4;
public class WhileLoop {
    public static void main(String[] args) {
        // while - when we do not know in advance
        // how many times we want to repeat block of code

        // I want to say hello 4 times
        int n = 1;

        while (n <= 4) {
            System.out.println("Hello");
            n++;
        }

    }
}